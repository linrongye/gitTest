package domain;


public class Article {

  private long artId;
  private String artNo;
  private String artTitle;
  private String artAuthor;
  private String artTime;
  private String artCollect;
  private String artComment;
  private String artContent;
  private String artLike;
  private String artUrl;


  public long getArtId() {
    return artId;
  }

  public void setArtId(long artId) {
    this.artId = artId;
  }


  public String getArtNo() {
    return artNo;
  }

  public void setArtNo(String artNo) {
    this.artNo = artNo;
  }


  public String getArtTitle() {
    return artTitle;
  }

  public void setArtTitle(String artTitle) {
    this.artTitle = artTitle;
  }


  public String getArtAuthor() {
    return artAuthor;
  }

  public void setArtAuthor(String artAuthor) {
    this.artAuthor = artAuthor;
  }


  public String getArtTime() {
    return artTime;
  }

  public void setArtTime(String artTime) {
    this.artTime = artTime;
  }


  public String getArtCollect() {
    return artCollect;
  }

  public void setArtCollect(String artCollect) {
    this.artCollect = artCollect;
  }


  public String getArtComment() {
    return artComment;
  }

  public void setArtComment(String artComment) {
    this.artComment = artComment;
  }


  public String getArtContent() {
    return artContent;
  }

  public void setArtContent(String artContent) {
    this.artContent = artContent;
  }


  public String getArtLike() {
    return artLike;
  }

  public void setArtLike(String artLike) {
    this.artLike = artLike;
  }


  public String getArtUrl() {
    return artUrl;
  }

  public void setArtUrl(String artUrl) {
    this.artUrl = artUrl;
  }

}
